/* 
Operadores Lógicos

&& -> AND   (E)
|| -> OR     (OU)
! -> NOT    (NÃO)

*/


console.log(true && true); // Retorna True, pois ambas as condições são verdadeiras
console.log(true || false); // Retorna True, pois uma das condições é verdadeira
console.log(!false); // Retorna True, pois a negação de False é True